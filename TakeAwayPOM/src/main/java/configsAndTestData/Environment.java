package configsAndTestData;

public class Environment {
	public String url;
	public String existingUserPassword;
	public String existingAddress;
	
		
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	public String existingUserEmailID;
	public String getExistingUserEmailID() {
		return existingUserEmailID;
	}
	public void setExistingUserEmailID(String existingUserEmailID) {
		this.existingUserEmailID = existingUserEmailID;
	}
	public String getExistingUserPassword() {
		return existingUserPassword;
	}
	public void setExistingUserPassword(String existingUserPassword) {
		this.existingUserPassword = existingUserPassword;
	}
	public String getExistingAddress() {
		return existingAddress;
	}
	public void setExistingAddress(String existingAddress) {
		this.existingAddress = existingAddress;
	}
	
	
	
		
}
